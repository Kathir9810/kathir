package com.java;

public class For_Loop {
	public static void main(String[] args) {
		for (int i = 0; i < 3; i++) {
			//System.out.println(i);
			for (int j = i; j < 5; j++) {
				System.out.println(j);
				System.out.println(i);
			}
			
			for (int k = 0; k < 6; k++) {
				System.out.println(k);
			}
			
			
			
		}
	}

}
